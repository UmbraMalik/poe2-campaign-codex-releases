const translations = {
  ru: mergeTranslationTrees(baseTranslations.ru, translationOverrides.ru),
  en: mergeTranslationTrees(baseTranslations.en, translationOverrides.en)
} satisfies Record<AppLanguage, TranslationTree>;

function interpolate(template: string, params?: TranslationParams): string {
  if (!params) {
    return template;
  }

  return template.replace(/\{(\w+)\}/g, (_match, key) => {
    const value = params[key];
    return value === undefined || value === null ? '' : String(value);
  });
}

function resolveTranslationValue(
  language: AppLanguage,
  key: string
): TranslationValue | undefined {
  const segments = key.split('.');
  let current: TranslationValue | undefined = translations[language];

  for (const segment of segments) {
    if (!current || typeof current === 'string') {
      return undefined;
    }

    current = current[segment];
  }

  return current;
}

export function translate(
  language: AppLanguage,
  key: string,
  params?: TranslationParams
): string {
  const requestedLanguage = language === 'en' ? 'en' : 'ru';
  const fallbackLanguage = requestedLanguage === 'en' ? 'ru' : 'en';
  const directValue = resolveTranslationValue(requestedLanguage, key);
  const fallbackValue = resolveTranslationValue(fallbackLanguage, key);
  const finalValue = typeof directValue === 'string'
    ? directValue
    : typeof fallbackValue === 'string'
      ? fallbackValue
      : key;

  return interpolate(finalValue, params);
}

export function createI18n(language: AppLanguage | null | undefined) {
  const resolvedLanguage: AppLanguage = language === 'en' ? 'en' : 'ru';

  return {
    language: resolvedLanguage,
    isEnglish: resolvedLanguage === 'en',
    t: (key: string, params?: TranslationParams) => translate(resolvedLanguage, key, params)
  };
}

export type I18nShape = ReturnType<typeof createI18n>;
