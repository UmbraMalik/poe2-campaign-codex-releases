type TranslationValue = string | TranslationTree;

interface TranslationTree {
  [key: string]: TranslationValue;
}
type TranslationParams = Record<string, string | number | null | undefined>;

function mergeTranslationTrees(
  base: TranslationTree,
  overrides: TranslationTree
): TranslationTree {
  const result: TranslationTree = { ...base };

  for (const [key, value] of Object.entries(overrides)) {
    const existing = result[key];

    if (
      value &&
      typeof value === 'object' &&
      !Array.isArray(value) &&
      existing &&
      typeof existing === 'object' &&
      !Array.isArray(existing)
    ) {
      result[key] = mergeTranslationTrees(existing, value);
      continue;
    }

    result[key] = value;
  }

  return result;
}

